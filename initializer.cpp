bool Initializer::IsCompatible(libusb_device* dev, const std::vector<DeviceId>& allowed) {
    libusb_device_descriptor desc;
    if (libusb_get_device_descriptor(dev, &desc) < 0) return false;

    for (const auto& target : allowed) {
        if (desc.idVendor == target.vid && desc.idProduct == target.pid) {
            return true;
        }
    }
    return false;
}

libusb_device_handle* Initializer::PrepareDevice(libusb_device* dev) {
    libusb_device_handle* handle = nullptr;
    int rc;

    rc = libusb_open(dev, &handle);
    if (rc != 0) {
        std::cerr << "[-] libusb_open FAILED: " << libusb_error_name(rc) << std::endl;
        return nullptr;
    }

    for (int i = 0; i < 2; i++) {
        if (libusb_kernel_driver_active(handle, i) == 1) {
            rc = libusb_detach_kernel_driver(handle, i);
            if (rc != 0) {
                std::cerr << "[-] libusb_detach_kernel_driver " << i << " FAILED: " << libusb_error_name(rc) << std::endl;
            }
        }

        rc = libusb_claim_interface(handle, i);
        if (rc != 0) {
            std::cerr << "[-] libusb_claim_interface " << i << " FAILED: " << libusb_error_name(rc) << std::endl;
            libusb_close(handle);
            return nullptr;
        }
    }

    rc = libusb_set_interface_alt_setting(handle, 0, 0);
    if (rc != 0) std::cerr << "[-] Alt setting 0 FAILED: " << libusb_error_name(rc) << std::endl;

    rc = libusb_set_interface_alt_setting(handle, 1, 0);
    if (rc != 0) std::cerr << "[-] Alt setting 1 FAILED: " << libusb_error_name(rc) << std::endl;

    if ((rc = libusb_clear_halt(handle, 0x02)) != 0) 
        std::cerr << "[-] Clear halt 0x02 FAILED: " << libusb_error_name(rc) << std::endl;
    
    if ((rc = libusb_clear_halt(handle, 0x82)) != 0) 
        std::cerr << "[-] Clear halt 0x82 FAILED: " << libusb_error_name(rc) << std::endl;

    Init(handle);
    
    return handle;
}

std::string Initializer::GetDeviceId(libusb_device* dev) {
    char buf[16];
    snprintf(buf, sizeof(buf), "%03d:%03d", 
                libusb_get_bus_number(dev), 
                libusb_get_device_address(dev));
    return std::string(buf);
}