void Initializer::BulkCommand(libusb_device_handle* handle, const std::vector<uint8_t>& payload) {
int transferred = 0;
    int rc;

    // Use 0x02 for OUT (Command)
    rc = libusb_bulk_transfer(handle, 0x02, (unsigned char*)payload.data(), (int)payload.size(), &transferred, 1000);
    if (rc != 0) {
        std::cerr << "[!] Bulk Write ERROR on 0x02: " << libusb_error_name(rc) << std::endl;
        return;
    }

    std::this_thread::sleep_for(std::chrono::milliseconds(50));

    // Use 0x82 for IN (Response)
    uint8_t buffer[64]; 
    rc = libusb_bulk_transfer(handle, 0x82, buffer, sizeof(buffer), &transferred, 1000);
    if (rc != 0) {
        std::cerr << "[!] Bulk Read ERROR on 0x82: " << libusb_error_name(rc) << std::endl;
    }
    
    std::this_thread::sleep_for(std::chrono::milliseconds(50));
}

